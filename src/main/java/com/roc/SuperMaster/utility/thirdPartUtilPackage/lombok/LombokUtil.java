package com.roc.SuperMaster.utility.thirdPartUtilPackage.lombok;

import lombok.extern.slf4j.Slf4j;

/**
 * @ClassName LombokUtil
 * @Description Lombok常用方法
 * @Author WangPeng
 * @date 2022/4/24 14:01
 * @Version 1.0
 */
@Slf4j
public class LombokUtil {

    /**
     * lombok的方便之处是通过简单的注解形式来帮助我们简化消除一些必须要有但显得有点很臃肿的java代码的工具。
     *
     * 在使用的时候只需要加上简单的注解。
     *
     * 实际上在反编译的过程中通过注解省去的代码依旧存在，只是我们在使用的时候无需添加。
     */

    /**
     * 常用的lombok注解:
     *
     * 参考链接：https://www.jianshu.com/p/365ea41b3573
     *
     * @Data（包括了生成get/set，toString，无参构造，equals，hashCode等方法）
     *
     * @Getter
     * @Setter
     * @ToString
     * @EqualsAndHashCode
     * @NoArgsConstructor
     * @AllArgsConstructor
     * @RequiredArgsConstructor
     *
     * @Synchronized
     * @Builder
     *
     * @Wither
     * @Delegate
     */

}
